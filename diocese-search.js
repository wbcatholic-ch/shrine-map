function _districtAliases(city, district, options){
  options = options || {};
  const base = district.replace(/구$|군$/,'');
  const forms = [];
  if(!options.cityOnly){
    forms.push(base, district);
  }
  const cityForms = [city];
  if(options.cityFull) cityForms.push(options.cityFull);
  cityForms.forEach(function(c){
    forms.push(c + base, c + ' ' + base, c + district, c + ' ' + district);
  });
  return Array.from(new Set(forms.filter(Boolean)));
}

function _districtEntry(city, cityFull, district, diocese, gwangu, display, cityOnly){
  return {
    k: city + ' ' + district,
    display: display || (city + ' ' + district),
    d: diocese,
    g: gwangu,
    aliases: _districtAliases(city, district, {cityFull:cityFull, cityOnly:!!cityOnly})
  };
}

